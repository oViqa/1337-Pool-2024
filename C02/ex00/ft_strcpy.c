char	*ft_strcpy(char *dest,	char	*src)

{
	char	*ret_ptr;

	ret_ptr = dest;
	while (*src != '\0')
	{
		*dest = *src;
		dest++;
		src++;
	}
	*dest = '\0';
	return (ret_ptr);
}
